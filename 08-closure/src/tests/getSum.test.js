const getSum = require ('../getSum');

describe('getSum', () => {
    test('getSum 1 ', () => {
        expect(getSum('123','456')).toBe('579')
    })
    test('getSum 2 ', () => {
        expect(getSum('111111111111111111111111111111111111111111111111111',
                      '233333333333333333333333333333333333333333333333333'))
            .toBe('344444444444444444444444444444444444444444444444444')
    })

})